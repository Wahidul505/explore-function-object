var fruit = 'apple';
switch (fruit) {
  case 'mango':
    console.log('the fruit is mango');
    break;
  case 'banana':
    console.log('the fruit is banana');
    break;
  case 'apple':
    console.log('the fruit is apple');
    break;
  case 'orange':
    console.log('the fruit is orange');
    break;
  default:
    console.log('there is no such fruit');
    break;
}

